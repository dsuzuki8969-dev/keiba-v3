export function paraphraseTrainingComment(raw: string | null | undefined): string {
  // 2026-04-30: 著作権配慮の体言止め変換 (識別変換 + 完コピ回避)
  // 旧 MAP は中国語簡体字混入 + 意味破壊あり → 完全無効化（dead code として削除済み）
  // 本実装は SAFE_MAP の手動キュレーション + 「○○の気配」→「気配は○○」の正規表現変換のみ。
  if (!raw) return "";
  const s = raw.trim();
  if (!s) return "";
  // 1) SAFE_MAP 完全一致
  if (SAFE_MAP[s]) return SAFE_MAP[s];
  // 2) 句読点除去後の完全一致
  const stripped = s.replace(/[。、．，!！?？\s]+$/u, "");
  if (SAFE_MAP[stripped]) return SAFE_MAP[stripped];
  // 3) 「○○の気配」 → 「気配は○○」 (マスター指示の体言止めパターン)
  const m1 = stripped.match(/^(.+)の気配$/);
  if (m1) return `気配は${m1[1]}`;
  // 4) 辞書未登録は空 (マスター指示「強度に記載がなければ何も書かない」精神に従う)
  //    完コピ回避のため原文 (raw) は返さない。次セッションで SAFE_MAP を順次拡充予定。
  return "";
}
