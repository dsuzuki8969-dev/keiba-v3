def build_course_index(courses: list[CourseMaster]) -> dict[str, CourseMaster]:
    """course_id → CourseMaster のDictを返す"""
    return {c.course_id: c for c in courses}


def get_all_courses() -> dict[str, CourseMaster]:
    return build_course_index(ALL_COURSES)


