def _distance_bucket(distance: int) -> str:
    if distance < 1400:
        return "sprint"
    if distance < 1800:
        return "mile"
    if distance < 2200:
        return "middle"
    return "long"


def infer_pace_from_first3f(
    distance: int,
    surface: str,
    first_3f_sec: Optional[float],
    venue_code: Optional[str] = None,
) -> Optional[PaceType]:
    """
    前半3F(秒)からペースタイプを推定

    Args:
        distance: レース距離(m)
        surface: 芝 or ダート
        first_3f_sec: 前半3Fの秒数（NoneならNoneを返す）
        venue_code: 競馬場コード（固有閾値がある場合に優先使用）

    Returns:
        PaceType (H/M/S) または None
    """
    if first_3f_sec is None or first_3f_sec <= 0:
        return None
    bucket = _distance_bucket(distance)
    surf_norm = "芝" if "芝" in surface else "ダート"

    # 距離固有オーバーライドを最優先
    h_bound, s_bound = None, None
    if venue_code:
        dist_override = _VENUE_DISTANCE_OVERRIDES.get((venue_code, distance), {})
        if surf_norm in dist_override:
            h_bound, s_bound = dist_override[surf_norm]

    # フォールバック: 汎用閾値
    if h_bound is None:
        th = PACE_THRESHOLDS.get(bucket, PACE_THRESHOLDS["mile"])
        h_bound, s_bound = th.get(surf_norm, (35.0, 37.0))

    if first_3f_sec < h_bound:
        return PaceType.H
    if first_3f_sec < s_bound:
        return PaceType.M
    return PaceType.S


def normalize_pace_to_3level(pace: Optional[PaceType]) -> str:
    """
    PaceTypeをH/M/Sの文字列に正規化（集計用）
    """
    if pace is None:
        return "M"
    return pace.value if pace else "M"
